# Task 5
# Create a program that takes two numbers as input
# Then prints whether the first number is greater than, less than, or equal to the second number.

num1 = int(input("Enter the value of num1"))
num2 = int(input("Enter the value of num2"))

if num1 > num2:
    print('True')
else:
    print("False")

if num1 < num2:
    print('True')
else:
    print("False")

if num1 == num2:
    print('True')
else:
    print("False")
